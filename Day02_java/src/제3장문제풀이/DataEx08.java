package 제3장문제풀이;

/** 03장.자료형.pdf No.36쪽 8번문제 */

public class DataEx08 {

	public static void main(String[] args) {
		
		float f1 = 1F;		// A
		float f2 = 1.0;		// B	float 타입 값 뒤에는 접미사 f or F가 붙어야 한다.
		float f3 = '1';		// C
		float f4 = "1";		// D	"1"은 String 문자열 참조타입이다.
		float f5 = 1.0d;	// E	1.0값뒤에 붙는 접미사 d는 double 실수숫자 타입 값뒤에 붙어야 한다. 여기서는 접미사 f or F가 붙어야 한다.
		
		// 정답 : 올바른 변수 선언된 것 -> A,C

	}

}
